# Дополнить класс "квартира" такой же функцией.

class Квартира:
    def __init__(self, комнаты):
        self.комнаты = комнаты  

    def площадь_стен(self):
        общая_площадь = 0
        for комната in self.комнаты:
            общая_площадь += комната.площадь_стен()
        return общая_площадь
    
